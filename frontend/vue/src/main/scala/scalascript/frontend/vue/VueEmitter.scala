package scalascript.frontend.vue

import scala.annotation.nowarn
import scalascript.frontend.*
import scalascript.frontend.FetchJsonSignal

/** Two-pass emit (same shape as React / Solid):
 *
 *    1. Collect every `ReactiveSignal` referenced anywhere in
 *       the View tree.  Each becomes a `ref(init)` in `setup()`.
 *
 *    2. Emit a Vue 3 component with `setup` + `render`.  `setup`
 *       creates the refs and returns them on the proxy; `render`
 *       reads `this.x` (auto-unwrapped) and writes via
 *       `this.x = ...`.
 *
 *  Pattern produced:
 *  {{{
 *  import { ref, h, Fragment, createApp } from 'vue';
 *  const App = {
 *    setup() {
 *      const count = ref(0);
 *      return { count };
 *    },
 *    render() {
 *      return h('div', null, [
 *        h('button', { 'onClick': () => { this.count = this.count + 1; } }, '+'),
 *        h('span',   null, this.count)
 *      ]);
 *    }
 *  };
 *  createApp(App).mount('#app');
 *  }}}
 *  Note `this.count` not `count.value` — Vue's component proxy
 *  unwraps refs exposed from `setup`. */
@nowarn("cat=deprecation")
private[vue] object VueEmitter:

  def emit(root: View[?]): String =
    val signals     = collectSignals(root)
    val lists       = collectLists(root)
    val refs        = collectRefs(root)
    val fetchSigs      = collectFetchSignals(root)
    val dataTableSigs  = collectDataTableSignals(root)
    val usesPortal  = containsPortal(root)
    val sb = new StringBuilder
    sb ++= "// Generated by ScalaScript v1.18 — frontend-vue Phase A5 / A2e / A6.\n"
    sb ++= "// Signals + lists + DomRefs + Portals lowered to ref() / watch / Teleport.\n"
    val teleportImport  = if usesPortal then ", Teleport" else ""
    val watchImport     = if fetchSigs.nonEmpty || dataTableSigs.nonEmpty then ", watch" else ""
    val mountedImport   = if fetchSigs.nonEmpty || dataTableSigs.nonEmpty then ", onMounted" else ""
    sb ++= s"import { ref, h, Fragment$teleportImport$watchImport$mountedImport, createApp } from 'vue';\n"
    sb ++= "const App = {\n"
    sb ++= "  setup() {\n"
    signals.foreach { (name, initial) =>
      sb ++= s"    const $name = ref($initial);\n"
    }
    lists.foreach { (name, initial) =>
      sb ++= s"    const $name = ref($initial);\n"
    }
    // DataTable array state: ref([]) + initial fetch + watch on tick.
    dataTableSigs.foreach { sig =>
      val urlJs    = jsString(sig.fetchUrl)
      val tickName = sig.tickId
      sb ++= s"    const ${sig.id} = ref([]);\n"
      sb ++= s"    fetch($urlJs).then(r => r.json()).then(data => { ${sig.id}.value = data; });\n"
      sb ++= s"    watch($tickName, (t) => { if (t > 0) fetch($urlJs).then(r => r.json()).then(data => { ${sig.id}.value = data; }); });\n"
    }
    // FetchUrlSignal / FetchJsonSignal: ref state + onMounted fetch + watch on tick.
    fetchSigs.foreach {
      case fjs: FetchJsonSignal =>
        sb ++= s"    const ${fjs.id} = ref(null);\n"
        sb ++= s"    const ${fjs.id}_loading = ref(false);\n"
        sb ++= s"    const ${fjs.id}_loaded = ref(false);\n"
        sb ++= s"    const ${fjs.id}_error = ref(\"\");\n"
        val urlJs = jsString(fjs.fetchUrl)
        sb ++= s"    onMounted(() => { fetch($urlJs).then(r => r.json()).then(t => { ${fjs.id}.value = t; }); });\n"
        sb ++= s"    watch(${fjs.tickId}, (t) => { if (t > 0) fetch($urlJs).then(r => r.json()).then(t => { ${fjs.id}.value = t; }); });\n"
      case fs =>
        sb ++= s"    const ${fs.id} = ref(\"\");\n"
        val urlJs = jsString(fs.fetchUrl)
        sb ++= s"    onMounted(() => { fetch($urlJs).then(r => r.text()).then(t => { ${fs.id}.value = t; }); });\n"
        sb ++= s"    watch(${fs.tickId}, (t) => { if (t > 0) fetch($urlJs).then(r => r.text()).then(t => { ${fs.id}.value = t; }); });\n"
    }
    // A6 — DomRefs lower to setup-level `ref(null)`s.  Vue's template
    // ref system binds the underlying DOM element to the ref's .value
    // once mounted.  Expose on window for imperative user JS.
    refs.foreach { name =>
      sb ++= s"    const $name = ref(null);\n"
      sb ++= s"    if (typeof window !== 'undefined') window[${jsString(name)}] = $name;\n"
    }
    val fetchSigNames   = fetchSigs.flatMap { fs =>
      fs match
        case fjs: FetchJsonSignal =>
          List(fjs.id, s"${fjs.id}_loading", s"${fjs.id}_loaded", s"${fjs.id}_error")
        case fs => List(fs.id)
    }
    val allNames = signals.keys.toList ++ lists.keys.toList ++ dataTableSigs.map(_.id) ++ fetchSigNames ++ refs.toList
    val returned =
      if allNames.isEmpty then "{}"
      else "{ " + allNames.mkString(", ") + " }"
    sb ++= s"    return $returned;\n"
    sb ++= "  },\n"
    sb ++= "  render() {\n"
    sb ++= "    return "
    sb ++= renderView(root)
    sb ++= ";\n"
    sb ++= "  }\n"
    sb ++= "};\n"
    sb ++= "createApp(App).mount('#app');\n"
    sb.toString

  private def collectSignals(view: View[?]): scala.collection.mutable.LinkedHashMap[String, String] =
    val acc = scala.collection.mutable.LinkedHashMap.empty[String, String]
    def register(signal: ReactiveSignal[?]): Unit =
      if signal.isInstanceOf[FetchUrlSignal] then return  // handled by collectFetchSignals
      val name = signal.id
      if !name.matches("[A-Za-z_][A-Za-z0-9_]*") then
        throw new IllegalArgumentException(
          s"ReactiveSignal id '$name' must match [A-Za-z_][A-Za-z0-9_]*."
        )
      val initJs = jsLiteral(signal())
      acc.get(name) match
        case Some(existing) if existing != initJs =>
          throw new IllegalArgumentException(
            s"ReactiveSignal id '$name' registered twice with different " +
            s"initial values ($existing vs $initJs)."
          )
        case _ => acc.update(name, initJs)
    def walk(v: View[?]): Unit = v match
      case View.SignalText(signal, _) =>
        register(signal)
      case View.Element(_, _, events, children) =>
        events.foreach { (_, handler) =>
          handler match
            case EventHandler.SetSignalLiteral(signal, _) => register(signal)
            case EventHandler.IncrementSignal(signal, _)  => register(signal)
            case EventHandler.ToggleSignal(signal)        => register(signal)
            case _ => ()
        }
        children.foreach(walk)
      case View.Fragment(children) => children.foreach(walk)
      case View.ComponentInstance(component, props) =>
        walk(component.render(props.asInstanceOf[Nothing]))
      case View.Show(_, whenTrue, whenFalse) =>
        walk(whenTrue())
        walk(whenFalse())
      case View.ShowSignal(cond, whenTrue, whenFalse) =>
        register(cond)
        walk(whenTrue)
        walk(whenFalse)
      case View.For(items, render) =>
        items().foreach(item => walk(render(item)))
      case View.ForSignal(_, _, _, itemTemplate) => itemTemplate.foreach(walk)
      case View.Portal(_, children) => children.foreach(walk)
      case dt: View.DataTable =>
        dt.actions.foreach {
          case RowActionDef.RowDelete(_, _, tick, hOpt) => register(tick); hOpt.foreach(register)
          case RowActionDef.RowPost(_, _, _, _, tick, hOpt) => register(tick); hOpt.foreach(register)
          case RowActionDef.RowLink(_, sig, _)           => register(sig)
          case _                                         => ()
        }
        dt.columns.foreach {
          case FieldColumnDef(_, _, _, Some(RowActionDef.RowInlineEdit(_, _, _, tick, hOpt)), _, _) =>
            register(tick); hOpt.foreach(register)
          case _ => ()
        }
      case View.EditableCell(_, _, action) =>
        register(action.onSuccessTick); action.headers.foreach(register)
      // P2
      case View.Column(children, _, _, _)     => children.foreach(walk)
      case View.Row(children, _, _, _)        => children.foreach(walk)
      case View.Stack(children, _)            => children.foreach(walk)
      case View.ScrollView(child, _, _)       => walk(child)
      case View.SafeArea(child, _)            => walk(child)
      case View.KeyboardAvoiding(child)       => walk(child)
      case View.Animated(child, _, _)         => walk(child)
      case View.Styled(child, _)              => walk(child)
      case View.Adaptive(web, _, _, fallback) => web.foreach(walk); walk(fallback)
      case View.LazyList(items, render, _, _)    => items().foreach(item => walk(render(item)))
      case View.LazyGrid(items, render, _, _, _) => items().foreach(item => walk(render(item)))
      case View.ModelView(_, _, template, _)  => walk(template)
      case View.ForModel(_, _, _, template, _) => walk(template)
      case View.Button(label, action, _, _)   => walkHandler(action); walk(label)
      case View.TextInput(value, _, _, _, _)  => register(value)
      case View.Toggle(checked, _, _)         => register(checked)
      case View.Slider(value, _, _, _, _)     => register(value)
      case View.Picker(_, selected, _, _)     => register(selected)
      case View.TabBar(tabs, current, _) =>
        register(current); tabs.foreach(t => walk(t.content))
      case View.NavigationStack(routes, current, _) =>
        register(current); routes.values.foreach(fn => walk(fn()))
      case View.Sheet(content, isPresented)       => register(isPresented); walk(content)
      case View.AlertDialog(_, _, _, isPresented) => register(isPresented)
      case View.Form(child, onSubmit, _)  => walkHandler(onSubmit); walk(child)
      case View.FormField(_, value, _, _) => register(value)
      case _ => ()
    def walkHandler(h: EventHandler): Unit = h match
      case EventHandler.SetSignalLiteral(sig, _)         => register(sig)
      case EventHandler.IncrementSignal(sig, _)          => register(sig)
      case EventHandler.ToggleSignal(sig)                => register(sig)
      case EventHandler.InputChange(sig)                 => register(sig)
      case EventHandler.FetchAction(_, _, body, tick, _, hOpt) => register(body); register(tick); hOpt.foreach(register)
      case EventHandler.DeleteItem(_, _, tick, hOpt) => register(tick); hOpt.foreach(register)
      case EventHandler.ItemAction(_, _, _, tick, hOpt) => register(tick); hOpt.foreach(register)
      case EventHandler.SetFieldToSignal(sig, _)         => register(sig)
      case _ => ()
    walk(view)
    acc

  private def collectFetchSignals(view: View[?]): Seq[FetchUrlSignal] =
    val seen = scala.collection.mutable.LinkedHashMap.empty[String, FetchUrlSignal]
    def checkSig(sig: ReactiveSignal[?]): Unit = sig match
      case fs: FetchUrlSignal if !seen.contains(fs.id) => seen(fs.id) = fs
      case _ => ()
    def walk(v: View[?]): Unit = v match
      case View.SignalText(sig, _) => checkSig(sig)
      case View.Element(_, attrs, events, children) =>
        attrs.values.foreach { case AttrValue.Reactive(sig) => checkSig(sig); case _ => () }
        events.foreach { (_, handler) => handler match
          case EventHandler.FetchAction(_, _, body, tick, _, hOpt) => checkSig(body); checkSig(tick); hOpt.foreach(checkSig)
          case _ => ()
        }
        children.foreach(walk)
      case View.Fragment(children)                    => children.foreach(walk)
      case View.ShowSignal(cond, whenTrue, whenFalse) => checkSig(cond); walk(whenTrue); walk(whenFalse)
      case View.Show(_, whenTrue, whenFalse)          => walk(whenTrue()); walk(whenFalse())
      case View.For(items, render)                    => items().foreach(item => walk(render(item)))
      case View.ForSignal(_, _, _, itemTemplate)      => itemTemplate.foreach(walk)
      case View.Portal(_, children)                   => children.foreach(walk)
      case View.ComponentInstance(component, props)   => walk(component.render(props.asInstanceOf[Nothing]))
      case View.ModelView(signal, _, template, _)     => checkSig(signal); walk(template)
      case View.ForModel(_, _, _, template, _)        => walk(template)
      case _ => ()
    walk(view)
    seen.values.toSeq

  private def collectDataTableSignals(view: View[?]): Seq[FetchUrlSignal] =
    val seen = scala.collection.mutable.LinkedHashMap.empty[String, FetchUrlSignal]
    def walk(v: View[?]): Unit = v match
      case dt: View.DataTable =>
        dt.source match
          case TableDataSource.Remote(sig) if !seen.contains(sig.id) => seen(sig.id) = sig
          case _ => ()
      case View.Fragment(children)                    => children.foreach(walk)
      case View.Element(_, _, _, children)            => children.foreach(walk)
      case View.Column(children, _, _, _)             => children.foreach(walk)
      case View.Row(children, _, _, _)                => children.foreach(walk)
      case View.Stack(children, _)                    => children.foreach(walk)
      case View.Show(_, whenTrue, whenFalse)          => walk(whenTrue()); walk(whenFalse())
      case View.ShowSignal(_, whenTrue, whenFalse)    => walk(whenTrue); walk(whenFalse)
      case View.For(items, render)                    => items().foreach(item => walk(render(item)))
      case View.ScrollView(child, _, _)               => walk(child)
      case View.ModelView(_, _, template, _)          => walk(template)
      case View.ForModel(_, _, _, template, _)        => walk(template)
      case _ => ()
    walk(view)
    seen.values.toSeq

  private def collectLists(view: View[?]): scala.collection.mutable.LinkedHashMap[String, String] =
    val acc = scala.collection.mutable.LinkedHashMap.empty[String, String]
    def register(list: ReactiveSignalList[?]): Unit =
      val name = list.id
      if !name.matches("[A-Za-z_][A-Za-z0-9_]*") then
        throw new IllegalArgumentException(
          s"ReactiveSignalList id '$name' must match [A-Za-z_][A-Za-z0-9_]*."
        )
      val initJs = jsArrayLiteral(list.initial)
      acc.get(name) match
        case Some(existing) if existing != initJs =>
          throw new IllegalArgumentException(
            s"ReactiveSignalList id '$name' registered twice with different " +
            s"initial values ($existing vs $initJs)."
          )
        case _ => acc.update(name, initJs)
    def walk(v: View[?]): Unit = v match
      case View.ForSignal(list, _, _, itemTemplate) =>
        register(list)
        itemTemplate.foreach(walk)
      case View.Element(_, _, events, children) =>
        events.foreach { (_, handler) =>
          handler match
            case EventHandler.PushSignalLiteral(list, _) => register(list)
            case EventHandler.ClearSignalList(list)      => register(list)
            case EventHandler.RemoveSelfFromList(list)   => register(list)
            case _ => ()
        }
        children.foreach(walk)
      case View.Fragment(children) => children.foreach(walk)
      case View.ComponentInstance(component, props) =>
        walk(component.render(props.asInstanceOf[Nothing]))
      case View.Show(_, whenTrue, whenFalse) =>
        walk(whenTrue()); walk(whenFalse())
      case View.ShowSignal(_, whenTrue, whenFalse) =>
        walk(whenTrue); walk(whenFalse)
      case View.For(items, render) =>
        items().foreach(item => walk(render(item)))
      case View.Portal(_, children) => children.foreach(walk)
      case _ => ()
    walk(view)
    acc

  /** A6 — DomRef collection.  Each becomes a `ref(null)` in setup()
   *  + a template-ref binding on the element. */
  private def collectRefs(view: View[?]): scala.collection.mutable.LinkedHashSet[String] =
    val acc = scala.collection.mutable.LinkedHashSet.empty[String]
    def register(ref: WidgetRef): Unit =
      val name = ref.id
      if !name.matches("[A-Za-z_][A-Za-z0-9_]*") then
        throw new IllegalArgumentException(
          s"WidgetRef id '$name' must match [A-Za-z_][A-Za-z0-9_]*."
        )
      acc += name
    def walk(v: View[?]): Unit = v match
      case View.Element(_, attrs, _, children) =>
        attrs.values.foreach {
          case AttrValue.RefBinding(ref) => register(ref)
          case _ => ()
        }
        children.foreach(walk)
      case View.Fragment(children) => children.foreach(walk)
      case View.ComponentInstance(component, props) =>
        walk(component.render(props.asInstanceOf[Nothing]))
      case View.Show(_, whenTrue, whenFalse) =>
        walk(whenTrue()); walk(whenFalse())
      case View.ShowSignal(_, whenTrue, whenFalse) =>
        walk(whenTrue); walk(whenFalse)
      case View.For(items, render) =>
        items().foreach(item => walk(render(item)))
      case View.Portal(_, children) => children.foreach(walk)
      case View.ForSignal(_, _, _, itemTemplate) => itemTemplate.foreach(walk)
      case _ => ()
    walk(view)
    acc

  /** A6 — cheap walk used at emit time to decide whether to import
   *  `Teleport` from 'vue'.  Pulling the import in unconditionally
   *  is harmless but `containsPortal` keeps the generated bundle
   *  diff-free for portal-less apps. */
  private def containsPortal(view: View[?]): Boolean = view match
    case _: View.Portal => true
    case View.Element(_, _, _, children) => children.exists(containsPortal)
    case View.Fragment(children) => children.exists(containsPortal)
    case View.ComponentInstance(component, props) =>
      containsPortal(component.render(props.asInstanceOf[Nothing]))
    case View.Show(_, whenTrue, whenFalse) =>
      containsPortal(whenTrue()) || containsPortal(whenFalse())
    case View.ShowSignal(_, whenTrue, whenFalse) =>
      containsPortal(whenTrue) || containsPortal(whenFalse)
    case View.For(items, render) =>
      items().exists(item => containsPortal(render(item)))
    case View.ForSignal(_, _, _, itemTemplate) =>
      itemTemplate.exists(containsPortal)
    case _ => false

  private def renderView(view: View[?], itemCtx: Option[ReactiveSignalList[?]] = None, modelItemVar: String = ""): String = view match
    case View.Element(tag, attrs, events, children) =>
      val propsJs = renderProps(attrs, events, itemCtx, modelItemVar = modelItemVar)
      val childrenJs =
        if children.isEmpty then ""
        else ", [" + children.map(c => renderView(c, itemCtx, modelItemVar)).mkString(", ") + "]"
      s"h(${jsString(tag)}, $propsJs$childrenJs)"

    case View.TextNode(thunk) =>
      jsString(thunk())

    case View.SignalText(signal, _) =>
      // `this.x` — Vue's component proxy auto-unwraps the ref.
      "this." + signal.id

    case View.ItemText =>
      // A2e.2 — `String(item)` when inside a render-arrow's
      // map callback; literal '' outside (graceful no-op).
      if itemCtx.isDefined then "String(item)" else "''"

    case View.Fragment(children) =>
      if children.isEmpty then "null"
      else s"h(Fragment, null, [${children.map(c => renderView(c, itemCtx, modelItemVar)).mkString(", ")}])"

    case View.ComponentInstance(component, props) =>
      renderView(component.render(props.asInstanceOf[Nothing]), itemCtx, modelItemVar)

    case View.Show(cond, whenTrue, whenFalse) =>
      val branch = if cond() then whenTrue() else whenFalse()
      renderView(branch, itemCtx, modelItemVar)

    case View.ShowSignal(cond, whenTrue, whenFalse) =>
      // Ternary inside render — Vue re-runs render when the ref
      // (read via proxy) changes; the ternary re-evaluates.
      s"(this.${cond.id} ? ${renderView(whenTrue, itemCtx, modelItemVar)} : ${renderView(whenFalse, itemCtx, modelItemVar)})"

    case View.For(items, render) =>
      val realised = items().toList
      if realised.isEmpty then "null"
      else
        val parts = realised.map(item => renderView(render(item), itemCtx, modelItemVar))
        s"[${parts.mkString(", ")}]"

    case View.ForSignal(list, tag, attrs, itemTemplate) =>
      // A2e / A2e.2 — map over `this.<jsName>`.  Two flavours:
      //
      //  - simple form: `h(tag, propsWithKey, String(item))`.
      //  - rich form: emit the template recursively with
      //    `itemCtx = Some(list)`; inject `key: String(item)` on
      //    the template's Element root.
      itemTemplate match
        case None =>
          val tagJs = jsString(tag)
          val attrFields = attrs.flatMap { (k, av) =>
            attrValueJs(av).map(value => s"${jsString(k)}: $value")
          }
          val propsParts = "'key': String(item)" :: attrFields.toList
          val propsJs    = s"{ ${propsParts.mkString(", ")} }"
          s"this.${list.id}.map((item, index) => h($tagJs, $propsJs, String(item)))"
        case Some(template) =>
          val body = template match
            case View.Element(tag, attrs, events, children) =>
              val propsJs    = renderProps(attrs, events, Some(list), injectKey = true)
              val childrenJs =
                if children.isEmpty then ""
                else ", [" + children.map(c => renderView(c, Some(list), modelItemVar)).mkString(", ") + "]"
              s"h(${jsString(tag)}, $propsJs$childrenJs)"
            case other =>
              renderView(other, Some(list), modelItemVar)
          s"this.${list.id}.map((item, index) => $body)"

    case View.Portal(target, children) =>
      // A6 — Vue's Teleport renders its slot content into the DOM
      // element matched by the `to` selector.  Same shape as our
      // Portal: `h(Teleport, { to: target }, [...children])`.
      val targetJs = jsString(target)
      val childrenJs =
        if children.isEmpty then ""
        else ", [" + children.map(c => renderView(c, itemCtx, modelItemVar)).mkString(", ") + "]"
      s"h(Teleport, { 'to': $targetJs }$childrenJs)"

    case dt: View.DataTable =>
      renderView(DataTableLowering.lower(dt), itemCtx, modelItemVar)

    // ── P2 semantic View cases ─────────────────────────────────────────────────
    // Vue h() children must be in an array; style prop accepts CSS string.

    case View.Column(children, spacing, align, style) =>
      val gapCss = if spacing > 0 then s"; gap: ${spacing}px" else ""
      val base   = s"display: flex; flex-direction: column; align-items: ${StyleUtils.hAlignToCSS(align)}$gapCss"
      val css    = StyleUtils.mergeCSS(base, StyleUtils.styleToCSS(style))
      val kids   = children.map(c => renderView(c, itemCtx, modelItemVar)).mkString(", ")
      s"h('div', { style: ${jsString(css)} }, [$kids])"

    case View.Row(children, spacing, align, style) =>
      val gapCss = if spacing > 0 then s"; gap: ${spacing}px" else ""
      val base   = s"display: flex; flex-direction: row; align-items: ${StyleUtils.vAlignToCSS(align)}$gapCss"
      val css    = StyleUtils.mergeCSS(base, StyleUtils.styleToCSS(style))
      val kids   = children.map(c => renderView(c, itemCtx, modelItemVar)).mkString(", ")
      s"h('div', { style: ${jsString(css)} }, [$kids])"

    case View.Stack(children, style) =>
      val css  = StyleUtils.mergeCSS("position: relative", StyleUtils.styleToCSS(style))
      val kids = children.map(c => renderView(c, itemCtx, modelItemVar)).mkString(", ")
      s"h('div', { style: ${jsString(css)} }, [$kids])"

    case View.ScrollView(child, axis, style) =>
      val base = axis match
        case Axis.Horizontal => "overflow-x: auto; overflow-y: hidden"
        case Axis.Vertical   => "overflow-y: auto; overflow-x: hidden"
        case Axis.Both       => "overflow: auto"
      val css  = StyleUtils.mergeCSS(base, StyleUtils.styleToCSS(style))
      s"h('div', { style: ${jsString(css)} }, [${renderView(child, itemCtx, modelItemVar)}])"

    case View.Spacer(size) =>
      val css = size match
        case Some(px) => s"width: ${px}px; height: ${px}px; flex-shrink: 0"
        case None     => "flex: 1; align-self: stretch"
      s"h('div', { style: ${jsString(css)} })"

    case View.Divider(axis, style) =>
      val base = axis match
        case Axis.Vertical => "width: 1px; height: 100%; background: currentColor; opacity: 0.2; flex-shrink: 0"
        case _             => "width: 100%; height: 1px; background: currentColor; opacity: 0.2; flex-shrink: 0"
      val css = StyleUtils.mergeCSS(base, StyleUtils.styleToCSS(style))
      s"h('div', { role: 'separator', style: ${jsString(css)} })"

    case View.Text(content, style) =>
      val css = StyleUtils.styleToCSS(style)
      val styleField = if css.isEmpty then "null" else s"{ style: ${jsString(css)} }"
      s"h('span', $styleField, ${jsString(content())})"

    case View.Image(source, style) =>
      val src = source match
        case ImageSource.Url(href)           => jsString(href)
        case ImageSource.Asset(name)         => jsString(s"/assets/$name")
        case ImageSource.Base64(data, mime)  => jsString(s"data:$mime;base64,$data")
      val css = StyleUtils.styleToCSS(style)
      val styleField = if css.isEmpty then "" else s", style: ${jsString(css)}"
      s"h('img', { 'src': $src$styleField })"

    case View.Icon(name, style) =>
      val css = StyleUtils.styleToCSS(style)
      val styleField = if css.isEmpty then "" else s", style: ${jsString(css)}"
      s"h('span', { 'aria-hidden': 'true', 'data-icon': ${jsString(name)}$styleField }, ${jsString(name)})"

    case View.Button(label, action, enabled, style) =>
