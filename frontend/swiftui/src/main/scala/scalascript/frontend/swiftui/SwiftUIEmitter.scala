  private def escapeSwiftIdent(name: String): String =
    if swiftKeywords.contains(name) then s"`$name`" else name

  private def swiftStringLit(value: String): String =
    "\"" + swiftString(value) + "\""

  def swiftString(value: String): String =
    value.flatMap {
      case '\\' => "\\\\"
      case '"'  => "\\\""
      case '\n' => "\\n"
      case '\r' => "\\r"
      case '\t' => "\\t"
      case c if c.isControl => f"\\u{${c.toInt}%04X}"
      case c    => c.toString
    }

  private def swiftLiteral(value: Any): String =
    value match
      case null       => "nil"
      case s: String  => swiftStringLit(s)
      case b: Boolean => if b then "true" else "false"
      case n: Int     => n.toString
      case n: Long    => n.toString
      case n: Float   => n.toString
      case n: Double  => n.toString
      case other      => swiftStringLit(other.toString)

  private def swiftTypeAndDefault(value: Any): (String, String) =
    value match
      case b: Boolean  => ("Bool",   if b then "true" else "false")
      case _: Int      => ("Int",    value.toString)
      case _: Long     => ("Int",    value.toString)
      case _: Double   => ("Double", value.toString)
      case _: Float    => ("Double", value.toString)
      case s: String   => ("String", swiftStringLit(s))
      case _: Seq[?]   => ("[String]", "[]")
      case _           => ("String", "\"\"")

  private def toSwiftColor(color: Color): Option[String] =
    color match
      case Color.Hex(v) =>
        val hex = v.stripPrefix("#")
        if hex.length == 6 then
          val r = Integer.parseInt(hex.substring(0, 2), 16) / 255.0
          val g = Integer.parseInt(hex.substring(2, 4), 16) / 255.0
          val b = Integer.parseInt(hex.substring(4, 6), 16) / 255.0
          Some(f"Color(red: $r%.3f, green: $g%.3f, blue: $b%.3f)")
        else None
      case Color.Rgb(r, g, b)     => Some(f"Color(red: ${r/255.0}%.3f, green: ${g/255.0}%.3f, blue: ${b/255.0}%.3f)")
      case Color.Rgba(r, g, b, a) => Some(f"Color(red: ${r/255.0}%.3f, green: ${g/255.0}%.3f, blue: ${b/255.0}%.3f, opacity: $a%.3f)")
      case Color.Named("red")     => Some("Color.red")
      case Color.Named("blue")    => Some("Color.blue")
      case Color.Named("green")   => Some("Color.green")
      case Color.Named("black")   => Some("Color.black")
      case Color.Named("white")   => Some("Color.white")
      case Color.Named("gray") | Color.Named("grey") => Some("Color.gray")
      case Color.Named("orange")  => Some("Color.orange")
      case Color.Named("yellow")  => Some("Color.yellow")
      case Color.Named("purple")  => Some("Color.purple")
      case Color.Named("pink")    => Some("Color.pink")
      case Color.System(token)    => Some(systemColorToken(token))
      case _                      => None

  private def systemColorToken(token: String): String =
    token match
      case "background"      => "Color(UIColor.systemBackground)"
      case "surface"         => "Color(UIColor.secondarySystemBackground)"
      case "primary"         => "Color.primary"
      case "secondary"       => "Color.secondary"
      case "accent"          => "Color.accentColor"
      case "error"           => "Color.red"
      case "on-background"   => "Color.primary"
      case "on-surface"      => "Color.primary"
      case "on-primary"      => "Color.white"
      case "on-secondary"    => "Color.white"
      case "outline"         => "Color(UIColor.separator)"
      case "outline-variant" => "Color(UIColor.opaqueSeparator)"
      case _                 => "Color.primary"

  private def weightToSwift(weight: FontWeight): String =
    weight match
      case FontWeight.Thin       => ".ultraLight"
      case FontWeight.ExtraLight => ".light"
      case FontWeight.Light      => ".light"
      case FontWeight.Regular    => ".regular"
      case FontWeight.Medium     => ".medium"
      case FontWeight.SemiBold   => ".semibold"
      case FontWeight.Bold       => ".bold"
      case FontWeight.ExtraBold  => ".heavy"
      case FontWeight.Black      => ".black"
      case FontWeight.Custom(v)  => if v >= 700 then ".bold" else if v >= 600 then ".semibold" else ".regular"

  private def hAlignToSwift(align: HAlign): String =
    align match
      case HAlign.Start   => ".leading"
      case HAlign.Center  => ".center"
      case HAlign.End     => ".trailing"
      case HAlign.Stretch => ".center"

  private def vAlignToSwift(align: VAlign): String =
    align match
      case VAlign.Top     => ".top"
      case VAlign.Center  => ".center"
      case VAlign.Bottom  => ".bottom"
      case VAlign.Stretch => ".center"


