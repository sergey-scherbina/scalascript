  private def emitFetchTask(
    method:         String,
    url:            String,
    bodyId:         String,
    onSuccessTickId: String,
    clearBody:      Boolean,
    indent:         Int
  ): String =
    val pad  = " " * indent
    val pad2 = " " * (indent + 4)
    val pad3 = " " * (indent + 8)
    val urlLine    = s"""${pad2}guard let _url = URL(string: ${swiftStringLit(url)}) else { return }"""
    val successLine = s"${pad3}$onSuccessTickId += 1"
    val clearLine   = if clearBody then s"\n${pad3}$bodyId = \"\"" else ""
    val httpMethod  = method.toUpperCase
    if httpMethod == "GET" || httpMethod == "DELETE" then
      s"""${pad}Task { @MainActor in
         |$urlLine
         |${pad2}do {
         |${pad3}let (_, _) = try await URLSession.shared.data(from: _url)
         |$successLine$clearLine
         |${pad2}} catch {}
         |${pad}}""".stripMargin
    else
      s"""${pad}Task { @MainActor in
         |$urlLine
         |${pad2}var _req = URLRequest(url: _url)
         |${pad2}_req.httpMethod = ${swiftStringLit(httpMethod)}
         |${pad2}_req.setValue("application/json", forHTTPHeaderField: "Content-Type")
         |${pad2}_req.httpBody = $bodyId.data(using: .utf8)
         |${pad2}do {
         |${pad3}let (_, _) = try await URLSession.shared.data(for: _req)
         |$successLine$clearLine
         |${pad2}} catch {}
         |${pad}}""".stripMargin

