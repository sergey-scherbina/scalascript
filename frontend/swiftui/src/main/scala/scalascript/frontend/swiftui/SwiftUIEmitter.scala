      case View.For(items, r)                              => items().foreach(i => walk(r(i)))
      case View.ForSignal(_, _, _, tmpl)                   => tmpl.foreach(walk)
      case View.Form(ch, _, _)                             => walk(ch)
      case View.Styled(ch, _)                              => walk(ch)
