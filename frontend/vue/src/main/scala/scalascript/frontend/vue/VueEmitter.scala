  private def renderProps(
      attrs:        Map[String, AttrValue],
      events:       Map[String, EventHandler],
      itemCtx:      Option[ReactiveSignalList[?]],
      injectKey:    Boolean = false,
      modelItemVar: String = ""
  ): String =
    val attrFields = attrs.flatMap { (k, v) =>
      v match
        case AttrValue.RefBinding(ref) =>
          // A6 — Vue's template `ref` prop accepts the setup-time ref
          // object directly.  Vue assigns the DOM element to ref.value
          // after mount; ignore the user-picked attr key since Vue
          // reserves the `ref` name for this binding.
          Some(s"'ref': ${ref.id}")
        case _ =>
          attrValueJs(v).map { value =>
            // Vue accepts `class` natively (same as Solid; unlike React).
            s"${jsString(k)}: $value"
          }
    }
    val eventFields = events.flatMap { (eventName, handler) =>
      eventHandlerJs(eventName, handler, itemCtx, modelItemVar)
    }
    val keyField = if injectKey then List("'key': String(item)") else Nil
    val all = keyField ++ attrFields ++ eventFields
    if all.isEmpty then "null" else s"{ ${all.mkString(", ")} }"

  private def attrValueJs(av: AttrValue): Option[String] = av match
    case AttrValue.Str(value)     => Some(jsString(value))
    case AttrValue.Bool(value)    => Some(value.toString)
    case AttrValue.Num(value)     => Some(formatNumber(value))
    case AttrValue.Dynamic(read)    => Some(jsLiteral(read()))
    case AttrValue.Reactive(signal) => Some(s"this.${signal.id}")
    case AttrValue.Absent           => None
    case AttrValue.RefBinding(_)    => None  // handled inline in renderProps as the Vue `ref` prop

  private def eventHandlerJs(
      eventName:    String,
      handler:      EventHandler,
      itemCtx:      Option[ReactiveSignalList[?]],
      modelItemVar: String
  ): Option[String] =
    val onKey = onProp(eventName)
    // Arrow functions defined INSIDE render() lexically capture
    // render()'s `this` — which IS the Vue component proxy.  A
    // function expression instead would lose `this` because Vue's
    // DOM-listener invoker calls it with `this === null`.
    handler match
      case EventHandler.SetSignalLiteral(signal, value) =>
        val v = jsLiteral(value)
        Some(s"${jsString(onKey)}: () => { this.${signal.id} = $v; }")
      case EventHandler.IncrementSignal(signal, by) =>
        Some(s"${jsString(onKey)}: () => { this.${signal.id} = this.${signal.id} + $by; }")
      case EventHandler.ToggleSignal(signal) =>
        Some(s"${jsString(onKey)}: () => { this.${signal.id} = !this.${signal.id}; }")
      case EventHandler.PushSignalLiteral(list, value) =>
        val v = jsLiteral(value)
        Some(s"${jsString(onKey)}: () => { this.${list.id} = this.${list.id}.concat([$v]); }")
      case EventHandler.ClearSignalList(list) =>
        Some(s"${jsString(onKey)}: () => { this.${list.id} = []; }")
      case EventHandler.RemoveSelfFromList(list) =>
        // A2e.2 — inside an item template, capture the map-callback
        // `index` so the listener filters its own slot out.
        if itemCtx.exists(_.id == list.id) then
          Some(s"${jsString(onKey)}: () => { this.${list.id} = this.${list.id}.filter((_, i) => i !== index); }")
        else
          Some(s"/* '$eventName' is RemoveSelfFromList used outside an item template — no-op */")
      case EventHandler.FetchAction(method, url, body, tick, clearBody, hOpt) =>
        val urlJs     = jsString(url)
        val methodJs  = jsString(method)
        val clearJs   = if clearBody then s" this.${body.id} = '';" else ""
        val headersJs = hOpt.map(h => s", headers: JSON.parse(this.${h.id} || '{}')").getOrElse("")
        Some(s"${jsString(onKey)}: () => { fetch($urlJs, {method: $methodJs, body: this.${body.id}$headersJs})" +
          s".then(r => r.text()).then(_ => { this.${tick.id}++;$clearJs }); }")
      case EventHandler.InputChange(signal) =>
        Some(s"'onInput': (e) => { this.${signal.id} = e.target.value; }")
      case EventHandler.DeleteItem(idField, deleteUrl, tick, hOpt) if modelItemVar.nonEmpty =>
        val delUrlJs  = jsString(deleteUrl)
        val headersJs = hOpt.map(h => s", headers: JSON.parse(this.${h.id} || '{}')").getOrElse("")
        Some(s"${jsString(onKey)}: () => { fetch($delUrlJs, {method: 'POST', body: String($modelItemVar.$idField)$headersJs}).then(r => r.text()).then(_ => { this.${tick.id}++; }); }")
      case EventHandler.DeleteItem(_, _, _, _) =>
        Some(s"/* '$eventName' is DeleteItem outside ForModel context — no-op */")
      case EventHandler.ItemAction(method, url, payload, tick, hOpt) if modelItemVar.nonEmpty =>
        val bodyJs    = rowPayloadExpr(modelItemVar, payload)
        val urlJs     = jsString(url)
        val methodJs  = jsString(method)
        val headersJs = hOpt.map(h => s", headers: JSON.parse(this.${h.id} || '{}')").getOrElse("")
        Some(s"${jsString(onKey)}: () => { fetch($urlJs, {method: $methodJs, body: $bodyJs$headersJs}).then(r => r.text()).then(_ => { this.${tick.id}++; }); }")
      case EventHandler.ItemAction(_, _, _, _, _) =>
        Some(s"/* '$eventName' is ItemAction outside ForModel context — no-op */")
      case EventHandler.SetFieldToSignal(signal, fieldPath) if modelItemVar.nonEmpty =>
        Some(s"${jsString(onKey)}: () => { this.${signal.id} = String($modelItemVar.$fieldPath); }")
      case EventHandler.SetFieldToSignal(_, _) =>
        Some(s"/* '$eventName' is SetFieldToSignal outside ForModel context — no-op */")
      case EventHandler.Simple(_) | EventHandler.WithEvent(_) =>
        Some(s"/* '$eventName' is a JVM closure — A5 can't translate (richer IR coming later) */")

  /** Vue uses camelCase event props: 'click' → 'onClick'. */
  private def onProp(eventName: String): String =
    "on" + eventName.headOption.fold("")(_.toUpper.toString) + eventName.drop(1)

  private def jsLiteral(value: Any): String = value match
    case s: String  => jsString(s)
    case i: Int     => i.toString
    case l: Long    => l.toString
    case d: Double  => formatNumber(d)
    case f: Float   => formatNumber(f.toDouble)
    case b: Boolean => b.toString
    case null       => "null"
    case other      => throw new IllegalArgumentException(
      s"jsLiteral: unsupported value type ${other.getClass.getName} ($other).  " +
      "Supported: String / Int / Long / Double / Float / Boolean / null."
    )

  private def jsArrayLiteral(items: Seq[Any]): String =
    items.map(jsLiteral).mkString("[", ", ", "]")

  private def formatNumber(d: Double): String =
    if d == d.toLong.toDouble then d.toLong.toString else d.toString

  private def jsString(s: String): String =
    val sb = new StringBuilder
    sb += '\''
    s.foreach {
      case '\\' => sb ++= "\\\\"
      case '\'' => sb ++= "\\'"
      case '\n' => sb ++= "\\n"
      case '\r' => sb ++= "\\r"
      case '<'  => sb ++= "\\u003c"
      case c    => sb += c
    }
    sb += '\''
    sb.toString

  /** Build the JS body expression for a RowPayload. */
  private def rowPayloadExpr(itemVar: String, payload: RowPayload): String = payload match
    case RowPayload.Field(name)   => s"String($itemVar.$name)"
    case RowPayload.WholeRow      => s"JSON.stringify($itemVar)"
    case RowPayload.Fields(names) =>
      val entries = names.map(n => s"[${jsString(n)}, $itemVar.$n]").mkString(", ")
      s"JSON.stringify(Object.fromEntries([$entries]))"

  /** Build the JS expression for a FormattedField (kind-aware cell renderer). */
  private def formattedFieldExpr(ref: String, fieldPath: String, kind: ColumnKind): String =
    val raw = s"String($ref.$fieldPath)"
    kind match
      case ColumnKind.Text        => raw
      case ColumnKind.Date(fmtOpt) =>
        val opts = fmtOpt.map(f => s", ${jsString(f)}").getOrElse("")
        s"(v => { try { return new Date(v).toLocaleDateString(undefined$opts) } catch(e) { return v } })($raw)"
      case ColumnKind.Money(curOpt, locOpt) =>
        val cur = jsString(curOpt.getOrElse("USD"))
        val loc = jsString(locOpt.getOrElse("en-US"))
        s"(v => { try { return new Intl.NumberFormat($loc, {style:'currency', currency:$cur}).format(Number(v)) } catch(e) { return v } })($raw)"
      case ColumnKind.StatusBadge(colorMap) =>
        val mapJs   = colorMap.map { case (k, v) => s"${jsString(k)}: ${jsString(v)}" }.mkString("{", ", ", "}")
        val colExpr = s"(${if colorMap.isEmpty then "{}" else mapJs})[String($ref.$fieldPath)] || ''"
        s"h('span', { style: { background: $colExpr, padding: '2px 8px', borderRadius: '9999px', fontSize: '0.85em' } }, $raw)"
      case ColumnKind.Link(urlOpt) =>
        val href = urlOpt match
          case Some(tpl) => s"${jsString(tpl)}.replace(':value', String($ref.$fieldPath))"
          case None      => s"'#'"
        s"h('a', { href: $href }, $raw)"
