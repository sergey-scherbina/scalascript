  private def textOf(view: View[?]): String =
    view match
      case View.Text(c, _)      => c()
      case View.TextNode(v)     => v()
      case View.SignalText(s,_) => String.valueOf(s.apply())
      case View.Fragment(ch)    => ch.map(textOf).mkString
      case View.Styled(ch, _)   => textOf(ch)
      case View.Icon(n, _)      => n
      case _                    => "Button"

  def swiftIdent(name: String): String =
    name.filter(c => c.isLetterOrDigit || c == '_').capitalize match
      case ""  => "App"
      case str => if str.head.isDigit then s"App$str" else str

