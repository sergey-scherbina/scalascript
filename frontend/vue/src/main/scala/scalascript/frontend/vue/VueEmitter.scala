      val listExpr = if fieldPath.isEmpty then s"this.$bindingVar" else s"this.$bindingVar.$fieldPath"
      s"($listExpr || []).map(($itemVar, _idx) => h(Fragment, {'key': _idx}, $body))"

    case View.ModelText(varName, fieldPath, _) =>
